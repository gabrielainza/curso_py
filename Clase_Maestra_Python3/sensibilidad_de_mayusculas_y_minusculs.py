#Reglas al escribir variables. 
#1. Las variables en python deben empezar con una letra o subrayado.
#2. No pueden contener números ni símbolos especiales, excepto el guión bajo (_).
#3. Debe ser un nombre que no esté reservado por la lenguaje (como palabras clave del
#lenguaje como if y while).
#4. Si se utiliza más de una vez dentro de un programa, las variables tienen que tener nombres diferentes.
